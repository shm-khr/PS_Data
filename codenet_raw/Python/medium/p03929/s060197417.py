#
# 1 <= i < i+2 <= n
# 1 <= j < j+2 <= 7
#
#  7*(i+0)+(j+0)-7, 7*(i+0)+(j+1)-7, 7*(i+0)+(j+2)-7
#  7*(i+1)+(j+0)-7, 7*(i+1)+(j+1)-7, 7*(i+1)+(j+2)-7
#  7*(i+2)+(j+0)-7, 7*(i+2)+(j+1)-7, 7*(i+2)+(j+2)-7
#
# sum(j,i) = 9*(7*i+j+1)
#
# sum(j,i)%11 = k
#
# sum(1,i) = 9*(7*i+1+1) = 9*(7*i+2) = 63*i+18
# sum(2,i) = 9*(7*i+2+1) = 9*(7*i+3) = 63*i+27
# sum(3,i) = 9*(7*i+3+1) = 9*(7*i+4) = 63*i+36
# sum(4,i) = 9*(7*i+4+1) = 9*(7*i+5) = 63*i+45
# sum(5,i) = 9*(7*i+5+1) = 9*(7*i+6) = 63*i+54
#
# sum(1,i)%11 = (63*i+18)%11 = (8*(i%11)+7)%11
# sum(2,i)%11 = (63*i+27)%11 = (8*(i%11)+5)%11
# sum(3,i)%11 = (63*i+36)%11 = (8*(i%11)+3)%11
# sum(4,i)%11 = (63*i+45)%11 = (8*(i%11)+1)%11
# sum(5,i)%11 = (63*i+54)%11 = (8*(i%11)+10)%11
#

n, k = map(int, input().split())

tmp = [0] * 13

for i in range(1, 13):
    for j in range(1, 6):
        sum9 = 9 * (7 * i + j + 1)
        if sum9 % 11 == k:
            tmp[i % 11] += 1

ans = 0
if n - 2 <= 12:
    for i in range(1, n - 1):
        ans += tmp[i % 11]
else:
    ans = sum(tmp) * ((n - 3) // 11)
    z =  ((n - 3) // 11) * 11
    for i in range(1, n - z - 1):
        ans += tmp[(z + i) % 11]
    
print(ans)